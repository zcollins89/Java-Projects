
public class student {
	private String name;
	
	private int age;
	//set the name
	public void setName(String newName) {
		name = newName;
	}
	
	//get the name
	public String getName() {
		return name;
	}
	//set the age
	public void setAge(int setAge) {
		age = setAge;
	}
	
	//get the age
	public int getAge() {
		return age;
	}
}
